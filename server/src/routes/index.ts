export function mountRoutes(app: Express) {
  app.use('/api/auth', authRoutes);
  app.use('/api/users', authMiddleware, userRoutes);
  app.use('/api/clients', authMiddleware, clientRoutes);
  app.use('/api/invoices', authMiddleware, invoiceRoutes);
  app.use('/api/quotes', authMiddleware, quoteRoutes);
  app.use('/api/payments', authMiddleware, paymentRoutes);
  app.use('/api/dashboard', authMiddleware, dashboardRoutes);
  app.use('/api/settings', authMiddleware, settingsRoutes);
  app.use('/api/recurring', authMiddleware, recurringRoutes);
  app.use('/api/ai', authMiddleware, aiRoutes);
  app.use('/api/bank-accounts', authMiddleware, bankAccountRoutes);
  app.use('/api/transactions', authMiddleware, transactionRoutes);
  app.use('/api/chat', authMiddleware, chatRoutes);
  app.use('/api/jofotara', authMiddleware, jofotaraRoutes);
  app.use('/api/reports', authMiddleware, reportRoutes);
  app.use('/api/daily-summary', authMiddleware, dailySummaryRoutes);
  app.use('/api/email-templates', authMiddleware, emailTemplateRoutes);
  app.use('/api/tracking', trackingRoutes); // Public â€” no auth
  app.use('/api/email-log', authMiddleware, emailLogRoutes);
  app.use('/api/employees', authMiddleware, employeeRoutes);
  app.use('/api/payroll', authMiddleware, payrollRoutes);
  app.use('/api/partner-expenses', authMiddleware, partnerExpenseRoutes);
  app.use('/api/accounts', authMiddleware, chartOfAccountsRoutes);
}
